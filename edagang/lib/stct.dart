  //=============================================================================== Api Calling here

//CALLING STATE API HERE
// Get State information by API
  List statesList = ['select state..'];
  String _myState;

  String stateInfoUrl = 'https://shopapp.e-dagang.asia/api/lookup/state';
  Future<String> _getStateList() async {
    final SharedPreferences prefs = await SharedPreferences.getInstance();
    http.get(stateInfoUrl, headers: {
      'Authorization' : 'Bearer '+prefs.getString('token'),'Content-Type': 'application/json',
    }).then((response) {
      print(response.body);
      var data = json.decode(response.body);
      setState(() {
        statesList = data['state_name'].toString() as List;
      });
    });
  }

  // Get State information by API
  List citiesList;
  String _myCity;

  String cityInfoUrl =
      'http://cleanions.bestweb.my/api/location/get_city_by_state_id';
  Future<String> _getCitiesList() async {
    await http.post(cityInfoUrl, headers: {
      'Content-Type': 'application/x-www-form-urlencoded'
    }, body: {
      "api_key": '25d55ad283aa400af464c76d713c07ad',
      "state_id": _myState,
    }).then((response) {
      var data = json.decode(response.body);

      setState(() {
        citiesList = data['cities'];
      });
    });
  }
}